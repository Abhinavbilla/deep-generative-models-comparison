# Reconstruction loss Evaluation 

import torch
import torch.nn.functional as F

def compute_reconstruction_loss(model, dataloader, device):
    """
    Computes the average Mean Squared Error (MSE) reconstruction loss 
    over an entire dataset.
    """
    model.eval()
    total_loss = 0
    count = 0
    
    with torch.no_grad():
        for x, _ in dataloader:
            x = x.to(device)
            
            # Handle DataParallel if using multiple GPUs
            raw_model = model.module if hasattr(model, "module") else model
            
            # Forward pass: the VAE returns [reconstruction, input, mu, log_var]
            # We only need the reconstruction at index 0
            x_recon = raw_model(x)[0] 
            
            # Calculate pixel-wise MSE loss between original and reconstructed
            loss = F.mse_loss(x_recon, x)
            
            total_loss += loss.item()
            count += 1
            
    final_loss = total_loss / count
    print(f"\nFinal Average Reconstruction Loss (MSE): {final_loss:.4f}")
    
    return final_loss
