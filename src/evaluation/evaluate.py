# FID Score Evaluation 

import torch
from torchmetrics.image.fid import FrechetInceptionDistance
from tqdm import tqdm 

def compute_generative_fid(model, dataloader, device, max_images=5000):
    """
    Computes the Fréchet Inception Distance (FID) between real images from the 
    dataloader and fake images generated by the model's latent space.
    """
    model.eval()

    # Initialize the FID metric utilizing the 2048-dimensional Inception-v3 features
    fid = FrechetInceptionDistance(feature=2048).to(device)
    
    processed_images = 0
    print(f"Extracting features for {max_images} real vs. generated images...")

    with torch.no_grad():
        for x, _ in tqdm(dataloader, desc="FID Batch"):
            x = x.to(device)
            current_batch_size = x.size(0)
            
            # Handle DataParallel if using multiple GPUs
            raw_model = model.module if hasattr(model, "module") else model

            # Generate fake images from random noise
            fake_images_raw = raw_model.sample(num_samples=current_batch_size, current_device=device)

            # Denormalize images from [-1, 1] to [0, 255] and convert to uint8 for FID calculation
            real_images = ((x + 1.0) / 2.0 * 255.0).to(torch.uint8)
            fake_images = ((fake_images_raw + 1.0) / 2.0 * 255.0).to(torch.uint8)

            # Update FID metric states
            fid.update(real_images, real=True)
            fid.update(fake_images, real=False)

            processed_images += current_batch_size
            if processed_images >= max_images:
                break
                
    # Compute the final FID score
    final_score = fid.compute().item()
    
    # Reset internal state to avoid memory leaks
    fid.reset() 
    
    print(f"\nFinal Generative Fréchet Inception Distance (FID): {final_score:.4f}")
    return final_score

