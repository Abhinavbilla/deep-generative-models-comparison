# LPIPS Interpolation Smoothness Evaluation

import torch
from torchmetrics.image.lpip import LearnedPerceptualImagePatchSimilarity

def compute_interpolation_lpips(model, dataloader, device, num_steps=10, max_pairs=100):
    """
    Computes the average LPIPS score between consecutive interpolated images
    in the latent space. Lower scores indicate smoother transitions.
    """
    model.eval()
    
    # Initialize LPIPS using the standard VGG network
    # The 'normalize=True' flag expects input images to be in the [-1, 1] range
    lpips_metric = LearnedPerceptualImagePatchSimilarity(net_type='vgg', normalize=True).to(device)
    
    total_lpips = 0.0
    pair_count = 0
    
    with torch.no_grad():
        for x, _ in dataloader:
            x = x.to(device)
            
            # Split the batch in half to create distinct pairs (x1, x2) for interpolation
            if x.size(0) < 2:
                continue
            half_size = x.size(0) // 2
            x1, x2 = x[:half_size], x[half_size:2*half_size]
            
            raw_model = model.module if hasattr(model, "module") else model
            
            # 1. Get continuous latent representations
            # (e.g., z_e for VQ-VAE, or mu for Standard/InfoVAE)
            z1 = raw_model.encode(x1)[0] 
            z2 = raw_model.encode(x2)[0]
            
            alphas = torch.linspace(0, 1, steps=num_steps).to(device)
            batch_lpips = 0.0
            
            # Generate the baseline first frame (alpha = 0)
            # VQ-VAE specific: ensure you run z1 through the quantizer before decoding!
            prev_img = raw_model.decode(z1) 
            
            # 2. Interpolate and calculate LPIPS between consecutive frames
            for i in range(1, num_steps):
                alpha = alphas[i]
                
                # Linear interpolation in the continuous space
                z_interp = (1.0 - alpha) * z1 + alpha * z2
                
                # --- VQ-VAE SPECIFIC STEP ---
                # If testing VQ-VAE, pass z_interp through your codebook quantizer here
                # z_interp = raw_model.quantize(z_interp) 
                
                # Decode the current frame
                curr_img = raw_model.decode(z_interp)
                
                # Calculate LPIPS between frame (i) and frame (i-1)
                step_lpips = lpips_metric(curr_img, prev_img)
                batch_lpips += step_lpips.item()
                
                # Shift the current frame to become the previous frame for the next loop
                prev_img = curr_img
                
            # Average LPIPS for the interpolation path of this specific batch
            total_lpips += (batch_lpips / (num_steps - 1))
            pair_count += 1
            
            if pair_count >= max_pairs:
                break
                
    final_lpips = total_lpips / pair_count
    print(f"\nFinal Average Interpolation Smoothness (LPIPS): {final_lpips:.4f}")
    
    return final_lpips

