# Latent Utilization (Active Units) Evaluation

import torch

def compute_active_units(model, dataloader, device, threshold=0.01):
    """
    Computes the number of active latent units in a continuous VAE.
    An active unit is a latent dimension where the variance of its mean 
    across the entire dataset exceeds the specified threshold.
    """
    model.eval()
    all_mus = []
    
    print("Extracting latent means across the dataset...")
    with torch.no_grad():
        for x, _ in dataloader:
            x = x.to(device)
            raw_model = model.module if hasattr(model, "module") else model
            
            # Extract the mean (mu) from the encoder
            # Note: Adjust this line if your encode() function returns a different tuple
            mu, _ = raw_model.encode(x)
            
            # Store the means on CPU to save GPU memory
            all_mus.append(mu.cpu())
            
    # Concatenate all collected means along the batch dimension
    # Resulting shape: (total_samples, latent_dim)
    all_mus = torch.cat(all_mus, dim=0)
    
    # Calculate the variance of the mean across the dataset (dim=0)
    # Resulting shape: (latent_dim,)
    variances = torch.var(all_mus, dim=0)
    
    # Count how many dimensions clear the threshold
    active_units = (variances > threshold).sum().item()
    total_units = variances.size(0)
    utilization_percentage = (active_units / total_units) * 100
    
    print(f"\nActive Latent Units: {active_units} / {total_units} ({utilization_percentage:.2f}% Utilization)")
    
    return active_units, total_units, utilization_percentage

